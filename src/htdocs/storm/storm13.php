<div class="column">
  <h2>Peak Dst -93 nT</h2>
  <p>
    On 24 September a coronal mass ejection (CME), a concentrated blast of
    electrically-conducting solar-wind plasma and tangled magnetic-field lines,
    left an active region on the Sun defined by a disk-centered sunspot. The
    CME arrived at Earth on 26 September at 12:37 UT (08:37 Eastern). In
    characteristic fashion, this was seen as a positive geomagnetic
    disturbance, or sudden commencement, as registered by low-latitude USGS
    magnetometers and as recorded by the USGS storm-time disturbance index Dst.
    This is the signature of compression of the Earth’s magnetosphere by the
    pressure of the solar wind. The sudden commencement reached Dst = 37 nT,
    placing it in approximately in the 75th percentile (75% of sudden
    commencements are larger). NOAA reports that the solar wind of the CME
    reached 650 km/s, from a background of 350 km/s. What followed over the
    next 24 hours was the normal main-phase of a magnetic storm: a
    west-ward-directed, equatorial magnetospheric electric current was
    activated, giving a characteristic depression in low-latitude magnetic
    disturbance. This storm showed two depressions, one at about 26 September
    18:30 UT and one at about 27 September 00:00 UT, with an over-all maximum
    depth of Dst = -93 nT (70th percentile). It is noteworthy that at
    high-latitudes, where magnetic disturbance can be much greater in amplitude
    and intensity, the direction of the magnetic field at Barrow, the most
    northerly USGS observatory, changed by about 6.5 degrees over a period of 3
    hours. Somewhat smaller variation was observed at the Deadhorse observatory
    near Prudhoe, AK. Such variations, large enough to be seen on an ordinary
    compass, would have been a severe nuisance for directional-drilling
    operations on the North Slope. This storm also produced beautiful displays
    of aurora at high latitudes. As we leave the quiet period of the last solar
    minimum and head into the next solar maximum, over the next 3 or 4 years we
    can expect a significant increase in the occurrence frequency and size of
    magnetic storms.
  </p>

  <p>
    For perspective: The largest storm of the 20th century occurred on March,
    1989 Dst = -574 nT. This storm induced electric currents in the Earth’s
    crust that found their way through ground connections into the high-voltage
    Canadian Hydro-Québec power grid, causing transformer failure and resulting
    in the loss of electric power to more than 6 million people. The same storm
    also damaged and disrupted the operation of satellites and it severely
    disrupted GPS systems and over-the-horizon radio communication systems
    used by the US military. The storm of March 1989, large though it was,
    pales in comparison to that of September 1859, the largest storm in
    recorded history occurred, Dst = -1600 nT (approximately). If such a storm
    were to occur today, the economic impact to the United States because of
    disrupted technological systems could exceed $1 trillion
    (Baker et al., 2008).
  </p>

