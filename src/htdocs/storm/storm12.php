<div class="column">
  <h2>Peak Dst -66 nT</h2>
  <p>
    On 19 January a solar flare was observed in an active region surrounding a
    high-latitude sunspot. At about the same time, a coronal mass ejection
    (CME), a concentrated blast of electrically-conducting solar-wind plasma
    and tangled magnetic-field lines, was observed, estimated to be headed for
    Earth. The CME arrived at Earth on 22 January at 06:14 UT (02:15 Eastern).
    In characteristic fashion, this caused as a positive initial phase
    geomagnetic disturbance, which was registered by low-latitude USGS magnetic
    observatories and as recorded by the 4-station USGS storm-time disturbance
    index Dst. This initial phase is the signature of compression of the
    Earth’s magnetosphere by the pressure of the solar wind. NOAA reports that
    the solar wind of the CME reached 450 km/s, from a background of 325 km/s.
    The initial phase reached Dst = 62 nT (only 10% of initial phases are this
    large or larger). What followed over the next 24 hours was the normal
    main-phase of a small magnetic storm: a west-ward-directed, equatorial
    magnetospheric electric current was activated, giving a characteristic
    depression in low-latitude magnetic disturbance, with maximum depth of
    –Dst = 66 nT at 23 January at 00:45. NOAA reported a Kp= 5 magnetic index,
    a generic measure of mid-latitude activity for a single 3-hr period on 22
    January, corresponding to a “minor” magnetic storm. It is noteworthy that
    at high-latitudes magnetic disturbance can be much greater in amplitude and
    intensity. The direction of the magnetic field at Barrow, Alaska, the most
    northerly USGS observatory, changed by about 6.0 degrees over a period of
    15 minutes. Such variations, large enough to be seen on an ordinary
    compass, would have been a nuisance for directional-drilling operations on
    the North Slope. This storm also produced beautiful displays of aurora at
    high latitudes. As we leave the quiet period of the last solar minimum and
    head into the next solar maximum, predicted for 2013, and over the next 3
    years or so we can expect more storms, some of which will probably be much
    larger than the 23 January 2012 storm.
  </p>

  <p>
    On 23 January, another solar flare and CME was observed. This might cause
    another magnetic storm at Earth on late 24 January.
  </p>

  <p>
    For perspective: The largest storm of the 20th century occurred on March,
    1989 -Dst = 574 nT. This storm induced electric currents in the Earth’s
    crust that found their way through ground connections into the high-voltage
    Canadian Hydro-Québec power grid, causing transformer failure and resulting
    in the loss of electric power to more than 6 million people. The same
    storm also damaged and disrupted the operation of satellites and it
    severely disrupted GPS systems and over-the-horizon radio communication
    systems used by the US military. The storm of March 1989, large though it
    was, pales in comparison to that of September 1859, the largest storm in
    recorded history occurred, -Dst = 1600 nT (approximately). If such a storm
    were to occur today, the economic impact to the United States because of
    disrupted technological systems could exceed $1 trillion
    (Baker et al., 2008).
  </p>

