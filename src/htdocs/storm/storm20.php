  <p>
    On April 5, 2010, 09:48 UT the Galaxy 15 Intelsat communications
    geostationary satellite ceased responding to controllers on the ground.
    Months later, Intelsat continues to re-establish contact with the satellite,
    but it has begun to drift and the satellite, designed to operate through
    2022, might end up being completely lost. The probable cause of the problem
    were a series of relatively small but unusual solar and geommagnetic events
    that commenced some 2 days earlier. On April 3, during relatively quiet
    solar conditions, a flare was observed near an active region on the surface
    of the Sun. Soon thereafter, a moderate-size coronal mass of hot plasma and
    tangled magnetic-field lines was ejected towards the Earth. Two days later,
    this ejection was detected by the ACE monitoring satellite situated between
    the Earth and the Sun. In response, NOAA’s Space Weather Prediction Center
    issued a forecast for slightly elevated levels of geomagnetic activity and
    space-weather disturbance. USGS magnetometers operated at ground-based
    observatories recorded the arrival of the coronal mass ejection on April 5,
    2010, 08:45. A magnetic storm unfolded over the course of the next day, but
    USGS storm-time disturbance measures (Dst) showed that it never became very
    large. What was unusual about this event was a very abrupt magnetic-field
    reconfiguration, a magnetic substorm, that occurred just prior to the loss
    of contact with the Galaxy 15. USGS magnetic observatories in Alaska,
    California, Arizona, and Hawaii, as well as those operated by the Canadians,
    France, and New Zealand, showed that substorm activity was largely confined
    to eastern-Pacific longitudes – the same longitudinal position of the
    Galaxy 15. The substorm accelerated charged particles from the tail of the
    Earth’s magnetosphere. This probably caused a buildup of static electric
    charge on the Galaxy 15, which, when discharged either temporarily upset or
    permanently damaged electronic components in the satellite. Events like
    this normally occur during large magnetic storms, and we can expect an
    increase in their frequency during the up-and-coming solar maximum in 2013.
    That the Galaxy 15 anomaly occurred during modestly disturbed space-weather
    conditions highlights the necessity for both continuous real-time
    monitoring, such as that coordinated between the USGS and NOAA, and
    scientific research so that the hazardous effects of space-weather
    disturbance can be mitigated.
