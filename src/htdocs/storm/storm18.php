  <h2>Peak Dst -95 nT</h2>
  <p>
    This small-to-moderate, but highly complex, multiphase geomagnetic storm is
    related to a series of Earth-directed coronal mass ejections (CMEs) that
    launched from the sun starting on February 16th, accounting for the first
    significant/extended geomagnetic activity observed in nearly 8 months.
  </p>

  <p>
    The initial phase exhibited no classical "sudden commencement", likely
    because a period of southward interplanetary magnetic field (IMF) had
    already initiated magnetospheric convection, slowly reducing USGS'
    1-minute storm-time disturbance (Dst) index. The first CME reached the
    Earth's magnetopause shortly after 03:00Z on Feb. 19, and in spite of a
    rather weak shock, its northward orientation drove a moderate (~15nT)
    positive impulse in Dst. As the CME's magnetic flux rope rotated, IMF
    reoriented southward, eroding the magnetopause, driving strong
    magnetospheric convection, and ultimately reducing USGS Dst to nearly
    -100nT for the storm's main phase. A subsequent northward turning of the
    IMF (possibly another small CME) encouraged a quick recovery, returning Dst
    more than halfway to ambient conditions within ~8 hours. The Space Weather
    Prediction Center's (SWPC's) estimated planetary K index (Kp) rose to 6
    during this period.
  </p>

  <p>
    A second much stronger shock, likely associated with a solar filament
    eruption observed on February 18th, hit Earth's magnetopause shortly after
    03:00Z on Feb. 20th. The IMF associated with the previous CME had been
    gradually weakening, and its orientation random, so the relatively strong
    (~25nT) positive impulse in Dst observed at 03:20Z was largely due to
    magnetopause (i.e., Chapman-Ferraro) currents. This was short-lived as IMF
    turned sharply southward, northward, then southward again, driving a
    double-dip storm-within-a-storm where USGS Dst fell back to -63nT and
    -75nT, respectively. IMF then returned to ambient conditions, and the
    geomagnetic storm (re)entered a slow recovery phase for almost 48 hours.
    Again, SWPC Kp rose to 6 during this period.
  </p>

  <p>
    Finally, a third, slow-moving CME, likely launched from the sun on February
    20th, arrived at Earth shortly after 07:00Z on Feb. 23rd. Increased solar
    wind density and a weakly northward IMF orientation drove a moderate
    (~17nT) positive impulse, before the IMF reoriented southward, reducing
    main phase Dst to -50nT. SWPC Kp only rose to 4 during this period.
  </p>

