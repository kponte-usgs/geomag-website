  <h2>Peak Dst 0 nT</h2>
  <p>
    On 2014 September 9 and on September 10, the National Oceanic and
    Atmospheric Administration, Space Weather Prediction Center (NOAA SWPC)
    reported the occurrence of two solar flares at a disk-centered sunspot
    active region on the Sun. At about the same time, a coronal mass (CME), a
    concentration of electrically-conducting solar-wind plasma, was ejected
    from the active region towards the Earth. This was followed on September 10
    by an even more intense flare and energetic CME. In response to these
    observations, NOAA issued a warning for a magnetic storm commencing on
    September 12 and continuing into September 13. The first CME arrived at the
    Earth on 11 September at 23:47 UT (19:47 Eastern). In characteristic
    fashion, this was seen as a positive geomagnetic perturbation, or sudden
    commencement, as registered by low-latitude magnetometers operated by the
    Geomagnetism Program of the US Geological Survey (USGS); see the first “SC”
    for the San Juan magnetic observatory in the attached Figure (c). A
    geomagnetic sudden commencement is the result of compression of the Earth’s
    magnetosphere by the pressure of the solar wind. NOAA reported that the
    arrival of the first CME corresponded to solar wind velocity jumping from
    400 km/s to about 480 km/s. The second CME arrived on 12 September at
    15:57 UT (11:57 Eastern), and solar wind velocity jumped from 450 km/s to
    about 620 km/s. What followed over the next 24 hours was the normal
    main-phase of a magnetic storm: a west-ward-directed, equatorial
    magnetospheric electric current was activated, giving a characteristic
    depression in low-latitude magnetic disturbance, Figure (c). NOAA reported
    a Kp= 7 magnetic index, a generic measure of mid-latitude activity for a
    single 3-hr period, late on September 12, corresponding to a “strong”
    magnetic storm. It is noteworthy that at high-latitudes, Figure (a),
    magnetic disturbance was, as normal for a magnetic storm, much greater in
    amplitude. This storm also produced beautiful displays of aurora at
    mid-latitudes, as reported by various media outlets, including National
    Public Radio (link) and Sky and Telescope (link). The Sun is presently near
    its 11-year solar-cycle sunspot maximum, and while this particular cycle is
    relatively weak compared to the past few, additional storms of intensity
    comparable or larger than this one can be expected over the next couple
    years.
  </p>
