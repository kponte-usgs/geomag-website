  <h2>Peak Dst -195 nT</h2>
  <p>
    A magnetic storm commenced on St. Patrick’s Day, Tuesday, March 17, 2015 at
    04:45 UT with the arrival at Earth of a coronal mass ejection. The
    geomagnetic field responded at low latitudes with a relatively strong
    “sudden commencement” of nearly 50 nT, followed by a “main phase” and peak
    in –Dst of almost 200 nT. The NOAA Kp index, a measure of mid-latitude
    magnetic activity, reached a value of 8, qualifying as a severe magnetic
    storm. More details follow.
  </p>

  <p>
    NOAA’s Advanced Composition Explorer satellite (ACE), located on the
    Sun-Earth line at a distance approximately 1% of the distance from the
    Earth to the Sun, recorded an abrupt increase in solar-wind velocity from
    an ambient value near 400 km/s to about 500 km/s. Concomitant solar-wind
    ram pressure abruptly compressed the Earth’s magnetosphere. This generated
    eastward-flowing magnetopause electric currents and a positive perturbation
    in the horizontal component of the geomagnetic field recorded at
    low-latitude magnetic observatories around the world. In Figure 1 we show
    time series of the magnetic deviation from a quiet baseline in the
    northward (X), eastward (Y), and down (Z) directions, as well as the
    full-field (F), all obtained from the US Geological Survey magnetic
    observatory in Honolulu, HI. The relatively large (~50nT) sudden
    commencement is labeled “SC” in the figure.
  </p>

  <p>
    After this, ACE data indicate that the solar wind’s interplanetary magnetic
    field (IMF) was southward directed, an orientation that allows connection
    of the IMF onto northward pointing geomagnetic field lines. Once this
    connection occurred, the magnetosphere became strongly energetically
    coupled with the solar wind, enhancing the westward-flowing equatorial ring
    current of the inner magnetosphere. This process is generally recorded at
    low-latitude magnetic observatories as a diminishment in northward
    geomagnetic field, the longitudinal average of which is the Dst index. This
    storm saw a maximum –Dst of 195 nT, realized on March 17 at 23:00 UT and,
    so, by this measure this storm was the largest of this unusually subdued
    solar cycle. By comparison, in the last solar cycle, several storms had
    maximum –Dst exceeding 300 nT, and one exceeded 400 nT. The mid-latitude
    magnetic activity index Kp reached a value of 8, which by NOAA space
    weather scales qualifies as a severe storm.
  </p>

  <p>
    As was typical for an intense storm, geomagnetic activity was particularly
    intense at high latitudes. For example, at the Deadhorse observatory on the
    northshore of Alaska<sup>1</sup>, magnetic declination varied by about 9
    degrees over the course of this storm. The storm could literally be measured
    using a simple compass! Aurora were seen across Alaska, Canada, and many
    states in the lower continental United States. The storm attracted media
    attention, including reports published by the Washington Post, USA Today,
    and Christian Science Monitor.
  </p>

