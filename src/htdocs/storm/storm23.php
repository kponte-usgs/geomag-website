<div class="column">
  <h2>Peak Dst -195 nT</h2>
  <p>
    A “severe” storm occurred on June 21-23, 2015. This storm arose in response
    to variable solar-wind conditions that the Space Weather Prediction Center
    of the National Oceanic and Atmospheric Administration (SWPC, NOAA)
    associated with active region 2371 on the Sun. A series of 3 coronal mass
    ejections (CMEs) originated from this active region. These were subsequently
    recorded by NASA’s Advanced Composition Explorer (ACE) satellite, located on
    the Sun-Earth line at a distance approximately 1% of the distance from the
    Earth to the Sun, as abrupt enhancements in solar-wind velocity and density.
    With the arrival of the CMEs at the Earth on about June 21 16:45 UT,
    June 22 05:45 UT, and June 22 18:30 UT, concomitant increases in solar-wind
    ram pressure abruptly compressed the Earth’s magnetosphere. These generated
    eastward-flowing magnetopause electric currents and positive perturbations
    in the horizontal component of the geomagnetic field recorded at
    low-latitude magnetic observatories around the world, including at USGS
    observatories in Guam, Honolulu, and San Juan.
  </p>

  <p>
    After the arrival at Earth of the third CME, ACE data indicate that the
    solar wind’s interplanetary magnetic field (IMF) was southward directed, an
    orientation that is optimal for connection of the IMF onto geomagnetic field
    lines. Once this connection occurred, the magnetosphere was effectively
    opened up to the solar wind, allowing electrically charged, energetic
    particles to be deposited into the magnetotail and into the westward-flowing
    equatorial ring current of the inner magnetosphere. This process is recorded
    at low-latitude magnetic observatories as a general “main-phase”
    diminishment in horizontal geomagnetic disturbance, the longitudinal average
    of which is the Dst index. This storm saw a maximum –Dst of 195 nT, realized
    on June 23 at about 04:30 UT and, so, by this measure this storm was
    slightly smaller than the St. Patrick’s day storm of March 17, 2015. Still,
    this storm is the second largest of this unusually subdued solar cycle. By
    comparison, in the last solar cycle, several storms had maximum –Dst
    exceeding 300 nT, and one exceeded 400 nT. The mid-latitude magnetic
    activity index Kp reached a value of 8 (one less than the maximum of 9).
  </p>

  <p>
    As was typical for an intense storm, geomagnetic activity was particularly
    intense at high latitudes. For example, at the Barrow observatory on the
    north shore of Alaska, magnetic declination varied by about 10 degrees over
    the course of this storm. Aurora were seen across Alaska, Canada, and many
    states in the lower continental United States.
  </p>

  <p>
    The storm attracted media attention, including reports published by the
    Washington Post, CBS News, NBC News, National Geographic, and Slate. A
    summary of this storm was also featured on the home page of the USGS.
  </p>

