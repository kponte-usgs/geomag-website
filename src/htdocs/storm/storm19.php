<div class="column">
  <h2>Peak Dst 30 nT</h2>
  <p>
    On February 14 a small magnetic storm occurred, generating a certain amount
    of interest in the press (e.g., The Washington Post, Feb. 17, 2011). The
    NOAA Space Weather Prediction Center, having observed a solar flare and a
    “coronal mass ejection” from an active region in the vicinity of a sunspot
    on Feb. 15, has forecast a 20% chance for a “minor” storm on Feb. 18. If
    such a storm occurs, it will be measured by the USGS geomagnetic
    observatory network, and USGS data will be used by both NOAA and the US Air
    Force to issue warnings so that mitigating steps can be taken by satellite
    and power-grid operators (among others). The Feb. 18 storm, if it occurs,
    can be expected to cause aurora that can be seen at night at high latitudes.
  </p>

  <p>
    From 2007 to about 2009, the sun was at a minimum in its 11-year cycle of
    variation. During this time, and as a result, geomagnetic activity was
    relatively quiescent. Now, however, we are in the midst of the early stages
    of a new solar cycle (number 24); solar activity, sunspot numbers, and
    geomagnetic activity have been increasing. We can expect that over the next
    few years, and especially at solar maximum around the years 2013 and 2014,
    that several large and potentially hazardous magnetic storms will occur.
  </p>

  <p>
    Magnetic storms result from the dynamic interaction of the solar wind and
    the coupled magnetospheric-ionospheric system of the Earth. Large magnetic
    storms represent a potential hazard for the activities and infrastructure
    of our modern, technologically based society. The possible effects of a
    large magnetic storm were the subject of a recent National Research Council
    report (Baker et al., 2008), which estimated that if a storm the size of
    the 1859 one were to occur today, damage to technological systems and
    resulting carry-on economic effects could cost the United States more than
    $1 trillion. Such storms can cause the loss of radio communication, reduce
    the accuracy of global-positioning systems, damage satellite electronics
    and affect satellite operations, increase pipeline corrosion, and induce
    voltage surges in electric power girds, causing blackouts. Even small
    storms can sometimes have surprising effects. A “substorm” on April 5,
    2010, resulted in the temporary loss of the Galaxy-15 communications
    satellite that operators are still trying to repair.
  </p>

  <p>
    Because the practical effects of space weather are realized on, or just
    above, the surface of the Earth, ground-based sensor systems, including the
    magnetic observatories of the USGS, play an important role in space-weather
    monitoring (Love 2008). The intensity of the Feb. 14 event was measured by
    the USGS storm-time disturbance index, Dst (Gannon et al., 2011), derived
    from global observatory data, including those collected by the USGS; -Dst
    had a maximum “main-phase” intensity of 30 nanoTesla (nT). For perspective,
    the largest storm of the 20th that of March 1989, had a maximum intensity
    of 525 nT, and the largest magnetic storm ever recorded, that of Sept.
    1859, had a maximum intensity of about 1600 nT. While the Feb. 14 storm was
    quite small, and the Feb. 18 event may not be too much larger, both are
    reminders of what we can expect in the coming years, and the vital role of
    the USGS magnetic observatories.
  </p>

