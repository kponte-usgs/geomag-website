<h2>Background</h2>
<p>
  The Geomagnetism Program established its first observatory at Cheltenham
  Maryland in 1900, when the Program was part of the Coast and Geodetic
  Survey and under the leadership of Drs Louis A. Bauer and John A.
  Fleming. The observatory was moved to Fredericksburg in 1956, a site
  which for many years served as the Program&#8217;s headquarters. Today,
  because it has produced high-quality data for so many years,
  Fredericksburg is one of the world&#8217;s most important observatories.
</p>
<p>
  In 1862 a major battle was fought at Fredericksburg Virginia during the
  American Civil War, a tragic event that cost over 15,000 lives, many of
  whom are buried at the nearby
  <a href="http://www.nps.gov/frsp/">Fredericksburg National Cemetery.</a>
</p>
