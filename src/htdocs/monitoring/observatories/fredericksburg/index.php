<h2>Other Operations</h2>

<ul>
  <li>
    A seismometer for the USGS <a href="http://www.anss.org/">Advanced
    National Seismic System</a> program
  </li>
  <li>
    A global positioning system (GPS) station, and other survey equipment,
    for the <a href="http://www.ngs.noaa.gov/">National Geodetic
      Survey</a> of NOAA, operated under terms of a memorandum of
    agreement with the USGS
  </li>
  <li>
    Network Operations Center (NOC) for the
    <a href="http://www.bie.edu">Bureau of Indian Education</a> (BIE)
    serving 183 elementary, secondary, residential and peripheral
    dormitories across 23 states and two (2) post-secondary schools:
    Haskell Indian Nations University and Southwestern Indian Polytechnic
    Institute.
  </li>
</ul>

