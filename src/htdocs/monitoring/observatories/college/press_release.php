<p>
  Research site renamed in honor of longtime geophysicist<br/>
  <br/>
  For Immediate Release<br/>
  July 20, 2009
  <br/><br/>
  FAIRBANKS, Alaska The University of Alaska Fairbanks has renamed a 46-acre
  research site in honor of its visionary and developer, Jack Townshend. The
  Jack Townshend College International Geophysical Observatory is a network of
  eight state-of-the-art facilities that collect geomagnetic, seismic and
  geophysical data to support research at the Geophysical Institute, the
  International Arctic Research Center, the U.S. Geological Survey, as well as
  other national and international organizations. The site is situated
  northwest of Smith Lake behind the UAF campus.
</p>
<p>
  Townshend, 82, is a long-time USGS geophysicist that has worked with the
  university research community for 46 years. He designed and developed CIGO,
  a project that took 10 years to complete, with encouragement from GI
  Director Roger Smith and Syun-Ichi Akasofu, former director of both the GI
  and IARC.
</p>
<p>
  Jack Townshend is a world leader as a creator of excellent observatories,
  Roger Smith said.
</p>
<p>
  The research site was completed in 1996. It is one of five scientific
  observatories Townshend has helped develop across the globe during his
  career that spans more than six-and-a-half decades.
</p>
<p>
  The renaming of the CIGO site is the third honor Townshend has received from
  UAF. In 1995 Townshend received an honorary doctorate of science from the
  university. Four years later an area of campus was renamed Townshend Point.
  Today, the site is marked by a large boulder and bronze plaque and is
  located at the crest of College Hill, near the University of Alaska Museum
  of the North.
</p>
<p>
  Jack Townshend was appointed as the special projects coordinator for the
  USGS Geomagnetism Program in Golden, CO. and principal consultant and
  advisor of CIGO in 2002. He continues to serve in those positions today.
</p>

