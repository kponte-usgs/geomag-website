<h2>Background</h2>
<p>
  The Geomagnetism Program established an observatory at Honolulu in 1902,
  when the Program was part of the Coast and Geodetic Survey and under
  the leadership of Drs Louis A. Bauer and John A. Fleming. The present
  observatory site was established in 1960. The observatory is operated
  for the USGS, under terms of a memorandum of agreement, by the
  <a href="http://www.prh.noaa.gov/pr/ptwc/">Pacific Tsunami Warning
  Center</a> of NOAA.
</p>
