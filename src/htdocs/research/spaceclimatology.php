<p>
  Space climatology is concerned with longer-term changes in the space
  environment that are driven almost entirely by changes in solar output. Data
  from ground-based magnetic observatories, including those of the USGS
  Geomagnetism Program, are an excellent proxy measure of near-Earth geospace
  conditions, with continuous records going back decades and, sometimes, even
  centuries. These data can be used to study quiet-time geomagnetic tides, and
  decades-to-centuries-long secular change in geomagnetic disturbance and
  magnetic-storm occurrence that is driven by solar-terrestrial interaction and
  which is modulated by the ~11 year sunspot cycle. Results from
  space-climatology research enhance our fundamental understanding of the Sun,
  the Earth and the surrounding space environment; they allow us to make
  long-term, probabilistic forecasts of space weather and magnetic storms; and
  they provide context in studies of global climate change.
</p>

